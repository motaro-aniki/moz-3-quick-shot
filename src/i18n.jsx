export const LanguageContext = createContext({ lang: 'ja', t: (k) => T.ja[k] ?? k });

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState('ja');

  useEffect(() => {
    ipcRenderer.invoke('get-config').then(cfg => {
      if (cfg.language) setLang(cfg.language);
    });
    const onLangChanged = (_, newLang) => setLang(newLang);
    ipcRenderer.on('language-changed', onLangChanged);
    return () => ipcRenderer.removeListener('language-changed', onLangChanged);
  }, []);

  const t = (key) => T[lang]?.[key] ?? T.ja[key] ?? key;
  return <LanguageContext.Provider value={{ lang, t }}>{children}</LanguageContext.Provider>;
}

export function useT() {
  return useContext(LanguageContext);
}
